void main() 
{
  
  // If and else statement
  var marks = 70;
  if (marks > 60)
  {
    print("You got first class passing marks");
  }
  else 
  {
    print("You need to work hard more");
  }
  
  // Ladder if else statments
  if(marks>90)
      print(" \n A+ ");
  else if (marks > 80 && marks < 90)
      print("\n A");
  else if(marks > 70 && marks < 80)
      print("\n B");
  else
  {
    print("\n work harder");
  }

}